package aIntroduction;
public class bVariables {
	
	public static void main(String args[]) {
		
	// We need to tell what data and how much space do we need
		
	// Short integer (short) - takes 2 bytes of memory
	// Integer (int) - takes 4 bytes of memory
	// Long integer (long) - takes 8 bytes of memory
		short s = 125;
		int i = 15;
		long l = 2345;
	// If we want to add int and long, the output will be long
		
	// Decimal number(float) takes 4 bytes of memory
	// Large decimal number(double) takes 8 bytes of memory
		double d = 1.23;
		float f ;
		
	// Character (char) takes 2 bytes
		char c = 's';
	
	// Boolean (boolean) takes 1/8 bytes = 1 bit
		boolean status = true;
		
	}

}
